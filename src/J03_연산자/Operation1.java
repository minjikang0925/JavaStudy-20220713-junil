package J03_연산자;

/*
 * Ctrl + space -> 자동완성
 * Ctrl + Alt + 화살표아래 -> 복사
 */
 

public class Operation1 {

	public static void main(String[] args) {
		
		int num = 50;
		
		System.out.println(++num);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		
		
		System.out.println(--num);
		System.out.println(num);
		System.out.println(num--);
		System.out.println(num);
		 
		/*
		 * 관계 연산자
		 * == 두 항이 같으면 참, 아니면 거짓
		 * != 두 항이 다르면 참, 아니면 거짓
		 * 
		 */
	}

}
